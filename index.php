                <p>O acervo contém dezenas de milhares de documentos sobre os mais diferentes momentos da história do Brasil e das relações entre o Brasil e os EUA. Por intermédio deles será possível encontrar um rico manancial de informações sobre os impactos da crise de 1929 no país, sobre a chegada de Getúlio Vargas ao poder em 1930, sobre o envolvimento do Brasil na Segunda Guerra Mundial, sobre a crise que levou à deposição de Vargas em 1945, sobre as transformações ocorridas no país no pós-guerra, sobre a crise que levou ao suicídio de Vargas em 1954, sobre as eleições de Jânio Quadros e sobre a sua renúncia. Por fim, há um vasto acervo relacionado à crise desencadeada com a renúncia de Jânio e sobre os difíceis anos do governo João Goulart.</p>
