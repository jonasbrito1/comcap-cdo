                <p>Em continuidade à primeira fase do Openning the Archives Project, nesta segunda etapa uma equipe de técnicos e estudantes, sob a liderança dos professores Sidnei J. Munhoz e James N. Green, digitalizou e indexou os documentos produzidos pela diplomacia dos EUA sobre o Brasil, cobrindo o período de 1908 a 1963. Estas atividades são resultado da parceria entre a Universidade Estadual de Maringá, a Brown University, o National Archives (EUA) e o Arquivo Nacional (Brasil).</p>
